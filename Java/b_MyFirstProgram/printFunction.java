public class printFunction{
    public static void main(String[] args) {
        System.out.println("Hello World"); // is basic print function 

        System.out.print("Hi, "); //ln means next line, without it the next print function will be put next to it
        System.out.println("People");

        System.out.print("\n" + "What's up buddy?" + "\n"); //adding \n will serve as next line, in a System.out.print funtion
        
        System.out.println("\t" + " This is is indent"); //adding \t will serve as tab in print function
    }
}